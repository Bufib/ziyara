export function getPlaceBySlug(slug?: string) {
  return allPlaces.find((place) => place.slug === slug);
}

export function getPlacesByCity(city?: string) {
  const normalizedCity = city?.trim().toLocaleLowerCase();
  if (!normalizedCity) return [];

  return allPlaces.filter((place) => place.city.toLocaleLowerCase() === normalizedCity);
}
